            <p>Que todos s&atilde;o a<strong>&nbsp;favor da inova&ccedil;&atilde;o</strong>, n&atilde;o h&aacute; d&uacute;vidas. Qual executivo ousaria se manifestar abertamente contra a inova&ccedil;&atilde;o dentro de sua organiza&ccedil;&atilde;o, sobretudo em tempos de dr&aacute;sticas mudan&ccedil;as, como estamos vivendo agora?<strong>&nbsp;Se ningu&eacute;m se op&otilde;e &agrave; inova&ccedil;&atilde;o, por que ela por vezes n&atilde;o acontece?</strong></p>
<p>Mesmo com todos a favor, a inova&ccedil;&atilde;o enfrenta diversos entraves dentro das organiza&ccedil;&otilde;es que impedem o seu nascimento, desenvolvimento e entrega de valor. Ao longo dos 16 anos de mercado da&nbsp;<strong><a href="https://inventta.net/" target="_blank" rel="noopener">Inventta</a></strong> e dos mais de 300 grandes projetos executados, temos vivenciado de perto os grandes desafios que impedem a inova&ccedil;&atilde;o. Destaco abaixo os 5 principais:</p>
<p>&nbsp;</p>
<h2><strong>Por que a inova&ccedil;&atilde;o por vezes n&atilde;o acontece?</strong></h2>
<h3><strong><br></strong><strong>DESAFIO #1: Falta de direcionamento</strong></h3>
<p>As organiza&ccedil;&otilde;es tendem a apegar-se a sua origem ao inv&eacute;s de buscar o seu destino, seu&nbsp;<strong>posicionamento no futuro</strong>. &Eacute; comum presenciarmos discuss&otilde;es de&nbsp;<strong><a href="https://inventta.net/servicos/ofertas-estrategia-de-inovacao/" target="_blank" rel="noopener">estrat&eacute;gia</a>&nbsp;</strong>de neg&oacute;cio entre l&iacute;deres que tentam enxergar o futuro atrav&eacute;s de uma proje&ccedil;&atilde;o linear com base no passado-presente. Quando a empresa vai bem, surge uma confian&ccedil;a inabal&aacute;vel no presente, no formato &ldquo;em time que se ganha n&atilde;o se mexe&rdquo;. Quando vai mal, surge um desejo de se voltar aos &ldquo;anos de ouro&rdquo;.</p>
<p>&Eacute; importante que as organiza&ccedil;&otilde;es tenham uma vis&atilde;o de futuro compartilhada pela lideran&ccedil;a para garantir a sustentabilidade de seus neg&oacute;cios e explorar novas oportunidades adjacentes. Essa vis&atilde;o de futuro deve direcionar as atividades de inova&ccedil;&atilde;o, no sentido de estimular a gera&ccedil;&atilde;o de ideias aderentes aos cen&aacute;rios visualizados.</p>
<blockquote>
<p>Quando o esfor&ccedil;o inovativo n&atilde;o &eacute; direcionado, o futuro da organiza&ccedil;&atilde;o est&aacute; em risco, pois geralmente ou o esfor&ccedil;o n&atilde;o resulta em nada, ou resulta em algo que n&atilde;o responde aos seus principais desafios estrat&eacute;gicos. E claro, o porqu&ecirc; de a inova&ccedil;&atilde;o muitas vezes n&atilde;o acontecer!</p>
</blockquote>
<p>&nbsp;</p>
<h3><strong>DESAFIO #2:&nbsp;</strong><strong>&Eacute; importante, mas n&atilde;o &eacute; urgente</strong></h3>
<p>Em pesquisa da Funda&ccedil;&atilde;o Dom Cabral, a maioria dos CEO&rsquo;s de empresas instaladas no Brasil concordam que inova&ccedil;&atilde;o &eacute; estrategicamente importante para seus neg&oacute;cios. Mas confessam que quando problemas operacionais acontecem, essa agenda &eacute; postergada.</p>
<p>Exemplos desse tipo de comportamento organizacional n&atilde;o faltam, sendo ainda poucas as empresas que possuem a pr&aacute;tica de inova&ccedil;&atilde;o como uma atividade cotidiana e relacionada com os processos de neg&oacute;cio da empresa.</p>
<blockquote>
<p>A inova&ccedil;&atilde;o como um processo de neg&oacute;cio &eacute; um dos principais fatores de sucesso das empresas atualmente. Basta olharmos para as startups mais inovadoras do mundo: elas em geral n&atilde;o possuem &ldquo;&aacute;rea de inova&ccedil;&atilde;o&rdquo;; a inova&ccedil;&atilde;o j&aacute; nasce presente em todos os processos do neg&oacute;cio, espalhada e fortemente atuante no dia-a-dia da empresa. E isso muda tudo.</p>
</blockquote>
<p>&nbsp;</p>
<h3><strong>DESAFIO #3:&nbsp;</strong><strong>Busca por uma receita gen&eacute;rica</strong></h3>
<p>Diversas organiza&ccedil;&otilde;es procuram a Inventta constantemente e nos perguntam qual &eacute; a receita para fazer a inova&ccedil;&atilde;o funcionar. Embora existam algumas boas pr&aacute;ticas que se aplicam a praticamente todas as organiza&ccedil;&otilde;es, n&atilde;o h&aacute; modelo &uacute;nico para a inova&ccedil;&atilde;o. E a tentativa de simplesmente copiar modelos sem uma avalia&ccedil;&atilde;o cr&iacute;tica e ajuste ao contexto da organiza&ccedil;&atilde;o em geral n&atilde;o &eacute; bem-sucedida.</p>
<p>Diferentes estrat&eacute;gias de neg&oacute;cio pedem diferentes modelos de inova&ccedil;&atilde;o. Diferentes culturas organizacionais pedem tamb&eacute;m diferentes modelos de organiza&ccedil;&atilde;o. N&atilde;o h&aacute; atalhos nessa jornada: em geral, empresas que possuem bons modelos de inova&ccedil;&atilde;o n&atilde;o escaparam do exerc&iacute;cio de olhar para dentro e entender quais as premissas para o melhor modelo de inova&ccedil;&atilde;o para seu contexto.</p>
<blockquote>
<p>Esse exerc&iacute;cio n&atilde;o precisa e n&atilde;o deve ser lento. Pelo contr&aacute;rio, h&aacute; diversas formas &aacute;geis de se encontrar um bom modelo de inova&ccedil;&atilde;o, com testes sucessivos para se chegar a um bom n&iacute;vel de entrega de resultado ao neg&oacute;cio em um per&iacute;odo de tempo relativamente curto. Mas esse exerc&iacute;cio precisa acontecer.</p>
</blockquote>
<p>&nbsp;</p>
<h3><strong>DESAFIO #4</strong><strong>:&nbsp;</strong><strong>Falta de ritmo</strong></h3>
<p>&Eacute; comum vermos as iniciativas de inova&ccedil;&atilde;o &ldquo;patinarem&rdquo; dentro das empresas. Competi&ccedil;&atilde;o com o dia-a-dia do neg&oacute;cio e falta de m&eacute;todos apropriados ao esfor&ccedil;o inovativo s&atilde;o as principais causas que levam a esse ritmo insuficiente da inova&ccedil;&atilde;o para entregar o valor esperado ao neg&oacute;cio.</p>
<p>N&atilde;o &eacute; poss&iacute;vel superar a competi&ccedil;&atilde;o e garantir a satisfa&ccedil;&atilde;o de clientes e consumidores com h&aacute;bitos e necessidades em constante mudan&ccedil;a apenas mantendo um mesmo ritmo de sempre. &Eacute; preciso fazer com que toda a organiza&ccedil;&atilde;o se mova mais r&aacute;pido que o &ldquo;vento contr&aacute;rio&rdquo; &agrave; inova&ccedil;&atilde;o.</p>
<blockquote>
<p>A inova&ccedil;&atilde;o precisa criar redund&acirc;ncias para n&atilde;o correr o risco de perder as batalhas internas do dia-a-dia das organiza&ccedil;&otilde;es. Enquanto uma empresa est&aacute; comercializando seu produto, outra empresa est&aacute; preocupada em torn&aacute;-lo obsoleto.</p>
</blockquote>
<p>&nbsp;</p>
<h3><strong>DESAFIO #5</strong><strong>:&nbsp;</strong><strong>Cultura e Engajamento<br></strong><strong>O &uacute;ltimo e muito importante&nbsp;</strong><strong>por que a inova&ccedil;&atilde;o n&atilde;o acontece</strong></h3>
<p>Inova&ccedil;&atilde;o &eacute; sobre pessoas. Percebemos o peso das pessoas por tr&aacute;s das grandes transforma&ccedil;&otilde;es, n&atilde;o apenas das inova&ccedil;&otilde;es executadas por executivos renomados das maiores empresas do mundo, mas tamb&eacute;m dos her&oacute;is da inova&ccedil;&atilde;o que encontramos em toda a jornada da Inventta e que n&atilde;o contam com holofotes sobre os resultados fant&aacute;sticos que alcan&ccedil;aram.</p>
<blockquote>
<p>A inova&ccedil;&atilde;o, por vezes n&atilde;o acontece, porque exige uma transforma&ccedil;&atilde;o organizacional que a inclua no centro da organiza&ccedil;&atilde;o. Toda transforma&ccedil;&atilde;o organizacional implica em uma transforma&ccedil;&atilde;o cultural, que por sua vez implica em transforma&ccedil;&atilde;o pessoal.</p>
</blockquote>
<p>Se as pessoas dentro da organiza&ccedil;&atilde;o n&atilde;o transformarem seus modelos mentais para incluir a inova&ccedil;&atilde;o no centro do neg&oacute;cio, dificilmente essa organiza&ccedil;&atilde;o ter&aacute; sucesso em sua jornada de inova&ccedil;&atilde;o.</p>
<p>&Eacute; preciso criar um ambiente prop&iacute;cio e f&eacute;rtil para nutrir novas ideias e conceitos, com times motivados e engajados em questionar ortodoxias para viabilizar o novo. O &ldquo;advogado do diabo&rdquo; que enfraquece novas ideias com os famosos &ldquo;isso n&atilde;o d&aacute; certo&rdquo; e &ldquo;j&aacute; fizemos e n&atilde;o funcionou&rdquo; deve ser abolido da parte criativa dos esfor&ccedil;os de inova&ccedil;&atilde;o. Isso n&atilde;o significa que dentro do processo n&atilde;o se deva ter etapas de foco e converg&ecirc;ncia; pelo contr&aacute;rio, elas s&atilde;o essenciais, mas no momento certo.</p>
<p><img style="display: block; margin-left: auto; margin-right: auto;" src="app/assets/img/posts/figura.webp" width="960" height="786"></p>
<p>&nbsp;</p>
<p>Embora os desafios sejam grandes, cada vez mais pessoas se juntam &agrave; jornada de inova&ccedil;&atilde;o das empresas. Esses &ldquo;loucos&rdquo; s&atilde;o os respons&aacute;veis por fazerem suas respectivas empresas correrem &ldquo;contra o vento&rdquo; e encontrarem sucesso em um mundo t&atilde;o inst&aacute;vel como o que estamos vivendo agora. Desafios existem para serem superados! E a&iacute;, voc&ecirc; concorda com os porqu&ecirc;s pelos quais a inova&ccedil;&atilde;o muitas vezes n&atilde;o acontece?</p>
<p>Quais desafios voc&ecirc; j&aacute; enfrentou na tentativa de fazer a inova&ccedil;&atilde;o acontecer dentro da sua organiza&ccedil;&atilde;o? Compartilha com a gente nos coment&aacute;rios para expandirmos os aprendizados.</p>
