            <p>O desafio passado pela lideran&ccedil;a para&nbsp;<strong>projetos de inova&ccedil;&atilde;o</strong>&nbsp;&eacute; muito claro e as<strong>&nbsp;expectativas</strong>&nbsp;sempre est&atilde;o l&aacute; no alto: &ldquo;contamos com a sua capacidade, energia e experi&ecirc;ncia para tornar nossa empresa&nbsp;<strong>excelente em inova&ccedil;&atilde;o.</strong>&nbsp;Queremos que a&nbsp;<a href="https://inventta.net/" target="_blank" rel="noopener"><strong>inova&ccedil;&atilde;o</strong></a>&nbsp;seja uma compet&ecirc;ncia-chave do nosso neg&oacute;cio. Toma aqui o seu or&ccedil;amento, conte com o nosso apoio e reporte-nos os resultados.&rdquo;</p>
<p>Em muitas situa&ccedil;&otilde;es &eacute; assim que come&ccedil;a a<strong>&nbsp;jornada de muitos gestores de inova&ccedil;&atilde;o</strong>: muita expectativa sobre o resultado do seu trabalho e&nbsp;pouco conhecimento da<strong><a href="https://inventta.net/a-inovacao-como-aliada-da-estrategia/" target="_blank" rel="noopener">&nbsp;lideran&ccedil;a</a>&nbsp;</strong>do que significa essa jornada &ndash; e que para muitas empresas &eacute; de uma completa transforma&ccedil;&atilde;o.</p>
<p>&nbsp;</p>
<p><img style="display: block; margin-left: auto; margin-right: auto;" src="app/assets/img/posts/basepath/Prancheta-2-1024x512.png.webp" alt="" width="736" height="368"></p>
<p>O jeito de se tomar uma&nbsp;<strong>decis&atilde;o de neg&oacute;cios tem mudado.</strong><br>Trenorol Trenbolone &ndash; Legal Steroids Bodybuilding tren weight gain buy anabolic mass gainer, buy anabolic steroids online south africa &ndash; me venda cada dia e n&atilde;o &eacute; apenas pela not&oacute;ria maior disponibilidade de dados e capacidade computacional para process&aacute;-los.</p>
<p><strong>Essa de fato &eacute; uma nova realidade e a ci&ecirc;ncia de dados tem conseguido elevar o patamar da qualidade e especificidade na tomada de decis&otilde;es a partir do uso, por exemplo, da intelig&ecirc;ncia artificial.</strong></p>
<p>Mas tem algo al&eacute;m dos dados que est&aacute; mudando no jeito de se tomar decis&atilde;o. O mundo do e<strong>mpreendedorismo e inova&ccedil;&atilde;o</strong>, que deu origem &agrave;s metodologias &aacute;geis, tem ensinado ao&nbsp;<strong>mundo dos neg&oacute;cios</strong> que ao inv&eacute;s de termos business plans robustos, com aquelas estimativas das mais &ldquo;confi&aacute;veis&rdquo; de demanda e receita, &eacute; melhor quebrar o plano em etapas e ir tomando a decis&atilde;o aos poucos, &agrave; medida em que mais informa&ccedil;&atilde;o esteja dispon&iacute;vel e as incertezas v&atilde;o diminuindo.</p>
<p>&nbsp;</p>
<p><img style="display: block; margin-left: auto; margin-right: auto;" src="app/assets/img/posts/basepath/Prancheta-3-1024x510.png.webp" alt="" width="767" height="382"></p>
<p>&Eacute; verdade que ser&iacute;amos injustos em desconsiderar que a ess&ecirc;ncia desse modo de tomada de decis&atilde;o est&aacute; pautada no que preconiza a teoria de op&ccedil;&otilde;es reais, j&aacute; estudada h&aacute; d&eacute;cadas por muitos, e que de forma muito inteligente foi apropriada pelo mundo empreendedor, onde a restri&ccedil;&atilde;o de recursos exige decis&otilde;es de aloca&ccedil;&atilde;o mais inteligentes dos mesmos.</p>
<p><img style="display: block; margin-left: auto; margin-right: auto;" src="app/assets/img/posts/basepath/mceu_19012128711713263258412.webp" width="805" height="555"></p>
<p>&nbsp;</p>
<p>Em outras palavras, ao inv&eacute;s de tomar uma&nbsp;<strong>grande decis&atilde;o,</strong>&nbsp;com um alto n&iacute;vel de incerteza e vinculada a um grande volume de recursos, tomam-s<strong>e pequenas decis&otilde;es ao longo do caminho,</strong>&nbsp;avan&ccedil;ando-se nas etapas e na aloca&ccedil;&atilde;o de recursos, &agrave; medida em que as incertezas v&atilde;o diminuindo.</p>
<p>Uma outra forma de ver o impacto dessa nova l&oacute;gica &eacute; observar a&nbsp;<strong>simula&ccedil;&atilde;o abaixo,</strong>&nbsp;que considera uma situa&ccedil;&atilde;o em que a Cia. tinha $1.200 de Budget para alocar, em 5 projetos que no total demandariam o investimento de $2.000</p>
<p>No modelo anterior de tomada de decis&atilde;o &ndash; que ainda impera em muitas organiza&ccedil;&otilde;es &ndash; a Cia. teria,&nbsp;<strong>sem maiores informa&ccedil;&otilde;es sobre as incertezas no caminho</strong>&nbsp;e qual projeto daria certo, que fazer uma escolha que necessariamente eliminaria de partida um dos projetos de<strong>&nbsp;maior potencial de impacto.</strong> E ao final do processo, menos de 30% dos recursos alocados nos projetos teria sido destinado a projeto exitosos.</p>
<p>&nbsp;</p>
<p><img style="display: block; margin-left: auto; margin-right: auto;" src="app/assets/img/posts/basepath/mceu_41033089421713263319404.webp" width="801" height="545"></p>
<p>J&aacute; no modelo atual, mais adequado aos&nbsp;<strong>projetos de inova&ccedil;&atilde;o,</strong>&nbsp;o cen&aacute;rio seria bem diferente.&nbsp;<strong>A primeira etapa de todos os projetos seria implementada e, &agrave; medida em que mais informa&ccedil;&otilde;es estivessem dispon&iacute;veis,</strong>&nbsp;mais f&aacute;cil seria a decis&atilde;o de se apostar na pr&oacute;xima etapa ou descontinuar o projeto.</p>
<p>Assim, al&eacute;m de se ter que apostar em apenas 1 projeto de alto potencial, seria poss&iacute;vel avan&ccedil;ar em ambos e ver qual teria maior potencial de sucesso ao longo da jornada. Nesse cen&aacute;rio, quase<strong>&nbsp;90% dos recursos alocados em projetos teriam sido destinados &agrave; projetos exitosos.</strong></p>
<p><img style="display: block; margin-left: auto; margin-right: auto;" src="app/assets/img/posts/basepath/mceu_55949683931713263378763.webp" width="805" height="548"></p>
<p>Por fim, encerramos esse artigo com uma importante ressalva. H&aacute; diversas raz&otilde;es para que n&atilde;o se aposte em&nbsp;<strong>todos o</strong>s&nbsp;<strong>projetos criados num processo de inova&ccedil;&atilde;o</strong>. Capacidade de lidar com todos eles ao mesmo tempo, grau de urg&ecirc;ncia, potencial de impacto, entre outros crit&eacute;rios.</p>
<p><strong>Ainda, &eacute; importante refor&ccedil;ar que os cen&aacute;rios acima s&atilde;o hipot&eacute;ticos</strong>&nbsp;pois, seria inimagin&aacute;vel considerar, por exemplo, que um gestor no primeiro cen&aacute;rio ao ver que um projeto n&atilde;o estivesse caminhando para o sucesso, n&atilde;o propusesse que mesmo fosse descontinuado. Ressalvas &agrave; parte, mesmo que com uma melhor otimiza&ccedil;&atilde;o dos recursos no primeiro cen&aacute;rio, tempo e energia seriam gastos de forma desnecess&aacute;ria revisitar or&ccedil;amentos, timeplans, etc.&nbsp;<strong>O objetivo dos cen&aacute;rios hipot&eacute;ticos acima apresentados &eacute; o de pintar em cores fortes as diferen&ccedil;as no modelo de tomada de decis&atilde;o.</strong></p>
