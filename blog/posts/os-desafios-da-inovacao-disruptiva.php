          <div class='content-blog position-relative overflow-hidden'>
            <p><strong>Produtos e servi&ccedil;os disruptivos</strong>&nbsp;s&atilde;o lan&ccedil;ados em um ritmo cada vez maior; por isso, &eacute; muito importante que as empresas n&atilde;o percam oportunidades de adapta&ccedil;&atilde;o.&nbsp;As grandes corpora&ccedil;&otilde;es normalmente contam com recursos financeiros,&nbsp;<strong><em>know-how</em></strong>&nbsp;tecnol&oacute;gico, l&iacute;deres e pessoas talentosas. No entanto, para explorar com<strong>&nbsp;efici&ecirc;ncia as oportunidades trazidas pelas transforma&ccedil;&otilde;es</strong>, &eacute; preciso ter tamb&eacute;m uma boa vis&atilde;o de quais s&atilde;o as<strong>&nbsp;compet&ecirc;ncias e defici&ecirc;ncias da empresa.</strong>&nbsp;A partir da&iacute;, &eacute; poss&iacute;vel entender quais tipos de mudan&ccedil;as a organiza&ccedil;&atilde;o &eacute; capaz de administrar para alcan&ccedil;ar a&nbsp;<strong>inova&ccedil;&atilde;o disruptiva.</strong></p>
<p><strong><img src="app/assets/img/posts/basepath/mceu_77949150611713233853371.webp"></strong></p>
<p>&nbsp;</p>
<p>Ao perguntar que<strong><a href="https://inventta.net/tipos-de-inovacao-inovando-em-oferta-e-experiencia/" target="_blank" rel="noopener">&nbsp;tipo de inova&ccedil;&atilde;o</a></strong>&nbsp;as empresas s&atilde;o, ou n&atilde;o, capazes de implementar com sucesso, &eacute; fundamental olhar para tr&ecirc;s fatores:&nbsp;<strong>recursos</strong>,&nbsp;<strong>processos</strong>&nbsp;e&nbsp;<strong>valores</strong>.&nbsp;</p>
<ul>
<li><strong>Recursos:</strong>&nbsp;s&atilde;o os aspectos que os gestores identificam mais instintivamente ao avaliar se suas organiza&ccedil;&otilde;es podem implementar com sucesso as mudan&ccedil;as que os confrontam. Incluem pessoas, equipamentos, tecnologia, conhecimento, rela&ccedil;&atilde;o com&nbsp;<em>stakeholders</em>, etc. No entanto, uma simples an&aacute;lise de recursos n&atilde;o &eacute; suficiente.</li>
<li><strong>Processos:&nbsp;</strong>definem o segundo fator que afeta a capacidade de uma empresa. Eles s&atilde;o criados para que as pessoas executem tarefas de forma consciente no dia-a-dia e n&atilde;o se espera que sejam mudados com frequ&ecirc;ncia. Quando os processos s&atilde;o aplicados na execu&ccedil;&atilde;o das tarefas para as quais foram projetados, &eacute; prov&aacute;vel que eles sejam praticados de maneira eficiente. Mas quando o mesmo processo aparentemente eficiente &eacute; empregado para lidar com uma tarefa muito diferente, &eacute; prov&aacute;vel que se mostre lento, burocr&aacute;tico e ineficiente. Um processo que cria a compet&ecirc;ncia para executar uma tarefa tamb&eacute;m pode definir sua pr&oacute;pria inefici&ecirc;ncia para executar outras.</li>
<li><strong>Valores:</strong>&nbsp;s&atilde;o os crit&eacute;rios pelos quais os profissionais tomam decis&otilde;es sobre as prioridades. Atrav&eacute;s deles &eacute; poss&iacute;vel julgar se um pedido &eacute; atraente ou n&atilde;o, se uma ideia para um novo produto &eacute; potencial ou marginal, se um investimento faz ou n&atilde;o sentido. Os valores s&atilde;o importantes porque permitem que qualquer funcion&aacute;rio atue de forma coerente com o restante da organiza&ccedil;&atilde;o e vincule sua a&ccedil;&atilde;o &agrave; estrat&eacute;gia.&nbsp;</li>
</ul>
<blockquote>
<p>&Agrave; medida que as empresas crescem, elas<strong>&nbsp;perdem a capacidade de entrar nos pequenos mercados emergentes</strong>, que ser&atilde;o os grandes mercados de amanh&atilde; e da inova&ccedil;&atilde;o disruptiva. Essa defici&ecirc;ncia n&atilde;o se deve apenas a uma mudan&ccedil;a nos recursos dentro das empresas, mas tamb&eacute;m atrav&eacute;s da evolu&ccedil;&atilde;o dos seus valores.</p>
</blockquote>
<p>&nbsp;</p>
<p><img src="app/assets/img/posts/basepath/mceu_23797437721713233956706.webp"></p>
<p>Com o tempo, o foco de uma organiza&ccedil;&atilde;o evolui entre cada fator citado acima: sua capacidade reside inicialmente em seus recursos, depois migra para processos e, finalmente, para valores. A constata&ccedil;&atilde;o de que as organiza&ccedil;&otilde;es evoluem por meio desses tr&ecirc;s est&aacute;gios foi feita por&nbsp;<strong><a href="https://claytonchristensen.com/" target="_blank" rel="noopener">Clayton Christensen</a>,</strong>&nbsp;especialista em inova&ccedil;&atilde;o, que foi professor na&nbsp;<strong>Harvard Business School.&nbsp;</strong></p>
<p>Contanto que o contexto corresponda &agrave;s situa&ccedil;&otilde;es para as quais os processos foram projetados e que os&nbsp;<a href="https://inventta.net/caminhos-para-disseminar-a-inovacao-por-toda-a-organizacao/" target="_blank" rel="noopener"><strong>valores da organiza&ccedil;&atilde;o&nbsp;</strong></a>possam direcionar o neg&oacute;cio, a<strong>&nbsp;empresa ter&aacute; um bom desempenho</strong>. A quest&atilde;o &eacute; que, como esses fatores tamb&eacute;m definem o que uma organiza&ccedil;&atilde;o n&atilde;o &eacute; capaz de realizar, surge uma dificuldade quando o ambiente muda.</p>
<p>&nbsp;</p>
<p><img src="app/assets/img/posts/basepath/a11-1024x512.png.webp" alt="" width="1024" height="512"></p>
<h3><strong>Inova&ccedil;&atilde;o Disruptiva&nbsp;</strong><strong><em>vs</em></strong><strong>&nbsp;Incremental&nbsp;</strong></h3>
<blockquote>
<p><br>A<strong>&nbsp;inova&ccedil;&atilde;o disruptiva</strong>&nbsp;refere-se a um produto, servi&ccedil;o ou conceito que perturba um<strong>&nbsp;mercado existente</strong>, ou cria um segmento de mercado completamente novo. Ela acontece quando os vetores de valor tradicionais em um mercado existente s&atilde;o alterados de forma significativa.&nbsp;</p>
</blockquote>
<p><strong>Quando a inova&ccedil;&atilde;o disruptiva entra no mercado, inicialmente atende apenas a um segmento de clientes pequeno e normalmente menos lucrativo, tornando-se inconsistente em rela&ccedil;&atilde;o aos valores defendidos pelas grandes corpora&ccedil;&otilde;es.</strong></p>
<p>As grandes empresas geralmente se concentram na<strong>s inova&ccedil;&otilde;es incrementais</strong>, que melhoram o&nbsp;<strong>desempenho</strong>&nbsp;de um produto ou servi&ccedil;o em aspectos que clientes de mercados convencionais j&aacute; valorizam. O investimento em inova&ccedil;&atilde;o sustent&aacute;vel (incremental) est&aacute; sempre alinhado com os valores das empresas, pois atrav&eacute;s dela &eacute; poss&iacute;ve<strong>l obter maiores margens de lucro.</strong></p>
<p><strong><img src="app/assets/img/posts/basepath/mceu_84835743131713234071607.webp"></strong></p>
<p>&nbsp;</p>
<p>Uma das raz&otilde;es pelas quais construir inova&ccedil;&otilde;es disruptivas &eacute; um desafio para grandes organiza&ccedil;&otilde;es deve-se ao fato dessas empresas dependerem de clientes e investidores para obter recursos. A cria&ccedil;&atilde;o de inova&ccedil;&otilde;es disruptivas envolve riscos maiores e as empresas estabelecidas n&atilde;o t&ecirc;m necessariamente um plano para o fracasso. Elas n&atilde;o est&atilde;o acostumadas a considerar seus esfor&ccedil;os de comercializa&ccedil;&atilde;o de uma tecnologia disruptiva como oportunidades de aprendizado.&nbsp;</p>
<p>&nbsp;</p>
<h3><strong>Como as grandes empresas conseguem enfrentar as mudan&ccedil;as?</strong>&nbsp;</h3>
<p>&nbsp;</p>
<p>Apesar de ser importante para o crescimento de qualquer neg&oacute;cio, implementar mudan&ccedil;as n&atilde;o &eacute; nada simples, e representa grandes impactos nos colaboradores &ndash; principalmente quando envolvem altera&ccedil;&atilde;o de processos e valores. De acordo com Christensen, as novas capacidades podem ser desenvolvidas atrav&eacute;s de tr&ecirc;s maneiras distintas:</p>
<p>&nbsp;</p>
<h5><strong>1) Desenvolvendo capacidades internamente</strong></h5>
<p>&nbsp;</p>
<p>Uma sa&iacute;da para que a inova&ccedil;&atilde;o disruptiva n&atilde;o esbarre em processos e valores pouco flex&iacute;veis, &eacute; criar um espa&ccedil;o organizacional &ldquo;seguro&rdquo;, que n&atilde;o iniba a criatividade e estimule mudan&ccedil;as &ndash; um grupo de trabalho isento de certas exig&ecirc;ncias corporativas.&nbsp;</p>
<p>Os processos tradicionais s&atilde;o extremamente valiosos para manter a sa&uacute;de dos neg&oacute;cios principais, mas n&atilde;o s&atilde;o capazes de transformar ideias em planos de neg&oacute;cios disruptivos. Por isso, as empresas precisam criar processos diferentes para avaliar e moldar ideias que rompem com o tradicional. Essa experi&ecirc;ncia ir&aacute; ajudar a equipe a desenvolver uma intui&ccedil;&atilde;o coletiva sobre como moldar planos de neg&oacute;cios disruptivos.</p>
<p>Al&eacute;m disso, ao criar um plano agregado, as empresas podem evitar que as propostas de inova&ccedil;&otilde;es incrementais concorram com ideias disruptivas por&nbsp;<em>budget</em>.</p>
<p><img src="app/assets/img/posts/basepath/mceu_469650041713234106774.webp"></p>
<h5><strong>2) Internalizando capacidades atrav&eacute;s de uma subsidi&aacute;ria</strong></h5>
<p>&nbsp;</p>
<p>Quando uma inova&ccedil;&atilde;o demanda que a empresa crie uma&nbsp;<strong>estrutura de custos diferente para ser competitiva,</strong>&nbsp;ou quando o tamanho da oportunidade n&atilde;o &eacute; significante diante da necessidade de<strong>&nbsp;crescimento da organiza&ccedil;&atilde;o principal,&nbsp;</strong><em>spin-outs</em>&nbsp;podem representar uma sa&iacute;da interessante para a&nbsp;<strong>constru&ccedil;&atilde;o de novos valores para explorar a mudan&ccedil;a.&nbsp;</strong></p>
<p>As iniciativas subsidi&aacute;rias s&atilde;o atividades pr&oacute;-ativas, aut&ocirc;nomas e de risco, que se originam fora da corpora&ccedil;&atilde;o central. Essas iniciativas, orientadas para o valor s&atilde;o tomadas a fim de avan&ccedil;ar e evoluir com novos neg&oacute;cios.&nbsp;<strong>Isso leva a uma maior autonomia da subsidi&aacute;ria,</strong>&nbsp;resultando em controle sobre o desenvolvimento de novos produtos, processos, penetra&ccedil;&atilde;o em novos mercados ou simplesmente&nbsp;<strong>a gera&ccedil;&atilde;o de novas ideias</strong> que t&ecirc;m aplica&ccedil;&otilde;es para outras partes da organiza&ccedil;&atilde;o principal.</p>
<p><img src="app/assets/img/posts/basepath/Prancheta-2-copia-2-1024x512.png.webp" alt="" width="1024" height="512"></p>
<h5><strong>3) Internalizando capacidades atrav&eacute;s de aquisi&ccedil;&otilde;es</strong></h5>
<p>Em muitos casos, uma fonte de inova&ccedil;&atilde;o &eacute; a aquisi&ccedil;&atilde;o de&nbsp;<strong><a href="https://inventta.net/startups-caminhos-para-a-ambidestria-organizacional/" target="_blank" rel="noopener"><em>s</em><em>tart ups</em></a></strong>&nbsp;(ou empresas de m&eacute;dio porte) que possuem os recursos e compet&ecirc;ncias essenciais para alavancar as grandes empresas em novos mercados.&nbsp;</p>
<p>No entanto, &eacute; necess&aacute;rio que as corpora&ccedil;&otilde;es saibam explorar as<strong><a href="https://inventta.net/como-criar-um-ambiente-propicio-a-inovacao/" target="_blank" rel="noopener">&nbsp;compet&ecirc;ncias adquiridas</a>&nbsp;de maneira a extrair todo o potencial.</strong>&nbsp;Se as compet&ecirc;ncias est&atilde;o nos processos e&nbsp;<strong>valores da empresa</strong> que foi adquirida, a &uacute;ltima coisa a ser feita &eacute; integrar a aquisi&ccedil;&atilde;o &agrave; organiza&ccedil;&atilde;o controladora. Se por outro lado, os recursos da empresa foram a raz&atilde;o principal para a aquisi&ccedil;&atilde;o, ent&atilde;o integrar a empresa &agrave; controladora pode fazer todo sentido &ndash; essencialmente conectar as pessoas, produtos, tecnologia e clientes adquiridos aos processos da organiza&ccedil;&atilde;o principal como uma forma de aproveitar as capacidades existentes.</p>
<p><img src="app/assets/img/posts/basepath/a4-1024x512.png.webp" alt="" width="1024" height="512"></p>
<p>&nbsp;</p>
<p>Por fim,<strong>&nbsp;entendemos que disrup&ccedil;&atilde;o significa romper com o curso normal de um processo; implica repens&aacute;-lo, reconfigur&aacute;-lo, fazer diferente do habitual.</strong>&nbsp;A<strong>&nbsp;chave</strong>&nbsp;para a<strong>&nbsp;inova&ccedil;&atilde;o disruptiva</strong> &eacute; a capacidade de repensar o modelo operacional existente e criar as condi&ccedil;&otilde;es certas para o surgimento de um novo. O primeiro passo para encontrar um caminho &eacute; compreender o problema, as capacidades e as defici&ecirc;ncias que est&atilde;o entranhadas na organiza&ccedil;&atilde;o.</p>
<p>&nbsp;</p>
