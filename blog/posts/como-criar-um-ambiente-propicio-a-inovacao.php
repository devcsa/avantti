          <div class='content-blog position-relative overflow-hidden'>
            <p>A din&acirc;mica de como a inova&ccedil;&atilde;o se d&aacute; dentro de uma organiza&ccedil;&atilde;o j&aacute; &eacute; estudada h&aacute; muitos anos e, embora sempre apontados como elementos dos modelos de inova&ccedil;&atilde;o, parece que apenas nos &uacute;ltimos anos a ficha caiu que a promo&ccedil;&atilde;o de um ambiente e cultura mais prop&iacute;cios a&nbsp;<a href="https://inventta.net/a-inovacao-como-aliada-da-estrategia/" target="_blank" rel="noopener">inova&ccedil;&atilde;o</a>&nbsp;&eacute; que torna de fato a capacidade de uma organiza&ccedil;&atilde;o inovar de forma&nbsp; sustent&aacute;vel no longo prazo.&nbsp;<strong>Mas, como criar um ambiente prop&iacute;cio &agrave; inova&ccedil;&atilde;o?</strong></p>
<p>Inspirado por uma conversa que tivemos durante um&nbsp;<a href="https://www.youtube.com/watch?v=8EQEKApDwCs" target="_blank" rel="noopener">webinar</a>&nbsp;que realizamos em agosto de 2020, durante o qual discutimos o tema&nbsp;<strong>&ldquo;Inovadores: o motor da inova&ccedil;&atilde;o&rdquo;</strong>, resolvemos colocar no papel nossa vis&atilde;o sobre as principais alavancas para se promover um ambiente prop&iacute;cio a inova&ccedil;&atilde;o. E sem mais delongas, descremos abaixo.</p>
<p>&nbsp;</p>
<h3><strong>1. Para criar um ambiente prop&iacute;cio &agrave; inova&ccedil;&atilde;o, estabele&ccedil;a e comunique um prop&oacute;sito claro para a inova&ccedil;&atilde;o.</strong></h3>
<p>O primeiro passo para uma organiza&ccedil;&atilde;o que deseja fortalecer os&nbsp;<a href="https://inventta.net/cultura-da-inovacao-como-catalisador-dos-resultados/" target="_blank" rel="noopener">elementos culturais que promovam a inova&ccedil;&atilde;o</a>&nbsp;&eacute; ter um objetivo, um prop&oacute;sito claro a seguir. Sem um claro entendimento de onde se quer chegar com o esfor&ccedil;o inovativo, sem uma vis&atilde;o que impulsione e inspire as mudan&ccedil;as, &eacute; dif&iacute;cil mover as pessoas para fora da zona de conforto.<strong>&nbsp;E sair da zona de conforto &eacute; necess&aacute;rio quando falamos de inova&ccedil;&atilde;o.</strong>&nbsp;Para alcan&ccedil;ar esse objetivo&nbsp;<strong>&eacute; importante que a comunica&ccedil;&atilde;o desse prop&oacute;sito seja clara,</strong>&nbsp;que alcance toda a organiza&ccedil;&atilde;o e que n&atilde;o haja desafino entre o que se diz aos belos ventos com o que a lideran&ccedil;a vivencia e d&aacute; como exemplo no dia-a-dia &ndash; 0 famoso &ldquo;walk the talk&rdquo;.</p>
<p><em><strong>Dica Inventta &gt;</strong>&nbsp;Crie uma estrat&eacute;gia e aspira&ccedil;&atilde;o de inova&ccedil;&atilde;o para a Cia.</em></p>
<p>&nbsp;</p>
<p><em><img style="display: block; margin-left: auto; margin-right: auto;" src="app/assets/img/posts/basepath/1.webp" alt="Estrategia" width="800" height="403"></em></p>
<h3><strong>2. Crie espa&ccedil;o para a experimenta&ccedil;&atilde;o e o erro.</strong></h3>
<p><strong>&Eacute; fato que ningu&eacute;m gosta de cometer erros</strong>,&nbsp;<strong>mas para inovar, errar &eacute; inevit&aacute;vel.</strong>&nbsp;At&eacute; mesmo porque, diferente daquele erro que se comete por displic&ecirc;ncia ou incompet&ecirc;ncia, o<strong>&nbsp;erro que se comete no processo inovativo &eacute; o que chamamos de &ldquo;erro bom&rdquo;</strong>&nbsp;pois sempre &eacute; utilizado como fonte de aprendizado e evolu&ccedil;&atilde;o. H&aacute; que se trabalhar os aspectos criam a capacidade de experimenta&ccedil;&atilde;o de uma organiza&ccedil;&atilde;o (ex.: budgets para teste, tabs de experimenta&ccedil;&atilde;o, compet&ecirc;ncias para prototipagem, etc.) mas tamb&eacute;m os elementos que promovam seguran&ccedil;a psicol&oacute;gica para que o &ldquo;erro bom&rdquo; seja reconhecido como parte do processo de evolu&ccedil;&atilde;o da capacidade de inova&ccedil;&atilde;o de uma organiza&ccedil;&atilde;o.</p>
<p><em><strong>Dica Inventta &gt;</strong>&nbsp;Defina e execute um pool de projetos inovadores que sirvam como refer&ecirc;ncia para formar a capacidade de experimenta&ccedil;&atilde;o na Cia. e exercite o processo de erro &ndash; aprendizado &ndash; evolu&ccedil;&atilde;o</em></p>
<p>&nbsp;</p>
<h3><strong>3. Promova a diversidade.</strong></h3>
<p>Como todos sabem a criatividade &eacute; parte importante do processo de inova&ccedil;&atilde;o e h&aacute; uma forte rela&ccedil;&atilde;o entre criatividade e&nbsp;<a href="https://inventta.net/inovacao-e-diversidade-sao-intrinsecas-mas-como-conecta-las-de-fato/" target="_blank" rel="noopener">diversidade</a>.&nbsp;Quanto maior a diversidade de experi&ecirc;ncias, modelos mentais, vis&atilde;o de mundo presentes num processo de inova&ccedil;&atilde;o,<strong>&nbsp;maior o espectro de possibilidades e a chance de solu&ccedil;&otilde;es e conceitos mais criativos e disruptivos.</strong>&nbsp;Para mudar o modelo mental dos colaboradores &eacute; importante mudar os v&iacute;nculos (<a href="https://www.youtube.com/watch?v=8EQEKApDwCs" target="_blank" rel="noopener">veja nosso webinar</a>). Times diversos e e multifuncionais ajudam a promover novos e ampliar os modelos de pensamento.</p>
<p><em><strong>Dica Inventta &gt;</strong>&nbsp;Forme squads multifuncionais e diversos para os projetos de inova&ccedil;&atilde;o</em></p>
<p>&nbsp;</p>
<h3><strong>4. Empodere as pessoas.</strong></h3>
<p>Os modelos hier&aacute;rquicos de gest&atilde;o que ainda persistem em muitas corpora&ccedil;&otilde;es, embora possam organizar o caos que &eacute; gerenciar diversos pap&eacute;is e responsabilidades, apresentem dois aspectos que jogam contra a inova&ccedil;&atilde;o: a lentid&atilde;o na tomada de decis&otilde;es e a intr&iacute;nseca falta de confian&ccedil;a nas pessoas. Sobre o primeiro ponto,&nbsp;<strong>vale dizer que por vezes velocidade &eacute; tudo para a inova&ccedil;&atilde;o</strong>. Sem a capacidade de responder rapidamente a uma nova tend&ecirc;ncia ou um movimento de um novo player no mercado &ndash; isso em fun&ccedil;&atilde;o das diversas intera&ccedil;&otilde;es e complexidades para a tomada de decis&atilde;o &ndash;&nbsp;<strong>uma boa oportunidade de inova&ccedil;&atilde;o pode n&atilde;o se tornar uma realidade, ou n&atilde;o acontecer no timing mais adequado.</strong>&nbsp;Mas o segundo ponto &eacute; mais importante quando se fala de<a href="https://inventta.net/cultura-da-inovacao-como-catalisador-dos-resultados/" target="_blank" rel="noopener">&nbsp;cultura de inova&ccedil;&atilde;o.</a>&nbsp;L&iacute;deres tem que aprender a confiar&nbsp;<em>nas</em>&nbsp;pessoas (compet&ecirc;ncia para fazer) e&nbsp;<em>&agrave;s</em>&nbsp;pessoas (capacidade de decidir) a&nbsp;<a href="https://inventta.net/jornada-da-inovacao-em-direcao-ao-resultado/" target="_blank" rel="noopener">jornada da inova&ccedil;&atilde;o</a>.&nbsp;L&iacute;deres de empresas com cultura forte de inova&ccedil;&atilde;o s&atilde;o felizes por n&atilde;o conseguirem controlar tudo o que acontece de inova&ccedil;&atilde;o a sua volta.</p>
<p><em><strong>Dica Inventta &gt;</strong>&nbsp;Estabele&ccedil;a desafios ou espa&ccedil;os de inova&ccedil;&atilde;o nos quais o time de inova&ccedil;&atilde;o ter&aacute; autonomia de decis&atilde;o. Quando se envolver no processo, coloque-se no mesmo n&iacute;vel deles.</em></p>
<p><em><img style="display: block; margin-left: auto; margin-right: auto;" src="app/assets/img/posts/basepath/2.webp" alt="Empodere" width="800" height="402"></em></p>
<h3><strong>5. Cultive a colabora&ccedil;&atilde;o.</strong></h3>
<p>Antes do termo inova&ccedil;&atilde;o aberta surgir, existia a cren&ccedil;a que era poss&iacute;vel uma organiza&ccedil;&atilde;o ter dentro de sua estrutura todo conhecimento necess&aacute;rio para inovar e gerar diferencial competitivo. Com o aumento da mobilidade das pessoas, globaliza&ccedil;&atilde;o e acesso as informa&ccedil;&otilde;es, ficou claro que isso n&atilde;o &eacute; poss&iacute;vel.<strong>&nbsp;Ter consci&ecirc;ncia das limita&ccedil;&otilde;es da organiza&ccedil;&atilde;o e estar aberto a colabora&ccedil;&atilde;o &eacute; essencial para suprir car&ecirc;ncias de capacidades e assim, gerar valor.</strong>&nbsp;Colabora&ccedil;&atilde;o &eacute; a nova m&aacute;xima da inova&ccedil;&atilde;o.</p>
<p><em><strong>Dica Inventta &gt;</strong>&nbsp;Identifique os gaps de compet&ecirc;ncia da organiza&ccedil;&atilde;o e crie formas de se conectar com outros atores que podem ajudar a supri-los (ex.: startups, academia, fornecedores, especialistas, etc.)</em></p>
<p>&nbsp;</p>
<h3><strong>6. Crie capacidades de inova&ccedil;&atilde;o.<br></strong></h3>
<p>S&oacute; brilho nos olhos n&atilde;o &eacute; suficiente para inovar. Al&eacute;m de&nbsp;<a href="https://inventta.net/como-formar-e-agrupar-um-time-de-inovacao/" target="_blank" rel="noopener">refor&ccedil;ar o time com pessoas inovadoras</a>&nbsp;(as vezes, tem que vir de fora), capacite o time em metodologias de inova&ccedil;&atilde;o, fortale&ccedil;a a capacidade de&nbsp;<em>problem-solving</em>&nbsp;das pessoas.</p>
<p><em><strong>Dica Inventta &gt;</strong>&nbsp;Nem sempre as capacidades v&ecirc;m antes na necessidade de inovar. Se preciso for, coloque o time para rodar o processo de inova&ccedil;&atilde;o e, &agrave; medida em que for identificando os gaps de capacidade, v&aacute; suprindo-os com treinamento, consultoria, estudo, benchmarking.</em></p>
<p><em><img style="display: block; margin-left: auto; margin-right: auto;" src="app/assets/img/posts/basepath/3.webp" alt="Inovacao" width="800" height="403"></em></p>
<h3><strong>7. Busque os quick-wins.<br></strong></h3>
<p><strong>Nem sempre o desafio mais relevante e importante estrategicamente para a empresa &eacute; o melhor para se come&ccedil;ar um processo de inova&ccedil;&atilde;o.</strong>&nbsp;Ter um&nbsp;<em>blend&nbsp;</em>de projetos dos diferentes quadrantes de uma matriz de complexidade de implanta&ccedil;&atilde;o vs. potencial de retorno &eacute; o caminho ideal para se promover um aprendizado diverso na Cia. mas tamb&eacute;m que garante ganhos r&aacute;pidos. Os ganhos r&aacute;pidos ajudam na motiva&ccedil;&atilde;o dos inovadores e na confian&ccedil;a da lideran&ccedil;a que esse caminho &eacute; bom para a Cia.</p>
<p><em><strong>Dica Inventta &gt;</strong>&nbsp;Nunca comece um processo de inova&ccedil;&atilde;o com poucos desafios ou apenas um desafio muito relevante estrategicamente para a Cia. Tenha como entrada para o processo de inova&ccedil;&atilde;o desafios &ldquo;mais f&aacute;ceis&rdquo; de resolver, mas que n&atilde;o inviabilizem o aprendizado (n&atilde;o pode ser t&atilde;o f&aacute;cil a ponto de restringir o aprendizado no processo de inova&ccedil;&atilde;o).</em></p>
<p>&nbsp;</p>
<p><em><img style="display: block; margin-left: auto; margin-right: auto;" src="app/assets/img/posts/basepath/4.webp" alt="quickwins" width="800" height="914"></em></p>
<p>Compartilhamos aqui, um pouco da experi&ecirc;ncia da <strong>Inventta</strong> nos projetos corporativos de inova&ccedil;&atilde;o que nos envolvemos.</p>
