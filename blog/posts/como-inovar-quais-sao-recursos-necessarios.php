<h3>Sobre a Avantt.i&nbsp;</h3>
<p><span class="TextRun SCXW195930854 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW195930854 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>A Avantt.i &eacute; uma plataforma de inova&ccedil;&atilde;o desenvolvida pela Inventta, para gestores de inova&ccedil;&atilde;o e gerentes de projeto.</p>
