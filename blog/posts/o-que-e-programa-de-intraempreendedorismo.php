</div>
<div class="OutlineElement Ltr SCXW150149248 BCX8" style="text-align: justify;">
<p class="Paragraph SCXW150149248 BCX8"><span class="TextRun SCXW150149248 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW150149248 BCX8"><span class="TextRun SCXW105312945 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW105312945 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>A ideia central do </span><span class="NormalTextRun SpellingErrorV2Themed SCXW150149248 BCX8">intraempreendedorismo</span><span class="NormalTextRun SCXW150149248 BCX8"> &eacute; que a empresa desenvolva comportamentos empreendedores em seus funcion&aacute;rios, n&atilde;o apenas para a cria&ccedil;&atilde;o de novos neg&oacute;cios, mas tamb&eacute;m para incutir um senso de propriedade e responsabilidade, permitindo que construam o futuro da empresa. No entanto, esse objetivo n&atilde;o &eacute; f&aacute;cil de alcan&ccedil;ar. Para criar um ambiente verdadeiramente empreendedor dentro da empresa, tr&ecirc;s aspectos devem ocorrer simultaneamente: o perfil dos colaboradores, a cultura organizacional e o papel da lideran&ccedil;a.</span></span><span class="EOP SCXW150149248 BCX8" data-ccp-props="{">&nbsp;</span></p>
</div>
<div class="OutlineElement Ltr SCXW150149248 BCX8" style="text-align: justify;">
<p class="Paragraph SCXW150149248 BCX8"><span class="TextRun SCXW150149248 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW150149248 BCX8"><span class="TextRun SCXW105312945 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW105312945 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>Quando falamos sobre o perfil do colaborador, queremos funcion&aacute;rios que, embora n&atilde;o sejam os donos da empresa, demonstrem um comportamento empreendedor como se fossem. Isso pode parecer ing&ecirc;nuo, mas &eacute; completamente poss&iacute;vel. Nem todos querem enfrentar os riscos e incertezas de ter um neg&oacute;cio pr&oacute;prio, mas muitos desejam empreender se essa instabilidade for reduzida. Como diz Flavio Augusto, a estabilidade &eacute; uma ilus&atilde;o, mas a instabilidade de empreender pode ser mitigada ao atuar como funcion&aacute;rio de uma empresa e empreender dentro dela.</span></span> <span class="EOP SCXW150149248 BCX8" data-ccp-props="{">&nbsp;</span></p>
</div>
<div class="OutlineElement Ltr SCXW150149248 BCX8" style="text-align: justify;">
<p class="Paragraph SCXW150149248 BCX8"><span class="TextRun SCXW150149248 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW150149248 BCX8"><span class="TextRun SCXW105312945 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW105312945 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>Estimular o comportamento empreendedor nos colaboradores significa permitir que cada um contribua para o futuro da empresa e tenha a iniciativa de transformar suas ideias em realidade, questionar e propor melhorias. No entanto, de nada adianta contratar funcion&aacute;rios com esse perfil se a cultura da empresa n&atilde;o apoiar esse comportamento. As pessoas se sentir&atilde;o desmotivadas e frustradas ao tentar implementar novas ideias sem ver progresso. Portanto, a cultura organizacional precisa favorecer o </span><span class="NormalTextRun SpellingErrorV2Themed SCXW150149248 BCX8">intraempreendedorismo</span><span class="NormalTextRun SCXW150149248 BCX8">.</span></span><span class="EOP SCXW150149248 BCX8" data-ccp-props="{">&nbsp;</span></p>
</div>
<div class="OutlineElement Ltr SCXW150149248 BCX8" style="text-align: justify;">
<p class="Paragraph SCXW150149248 BCX8"><span class="TextRun SCXW150149248 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW150149248 BCX8"><span class="TextRun SCXW105312945 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW105312945 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>Em um ambiente onde o </span><span class="NormalTextRun SpellingErrorV2Themed SCXW150149248 BCX8">intraempreendedorismo</span><span class="NormalTextRun SCXW150149248 BCX8"> &eacute; incentivado, n&atilde;o h&aacute; espa&ccedil;o para a estrutura tradicional de "comando e controle". A empresa deve promover e apoiar o comportamento empreendedor e estar disposta a correr riscos, de maneira controlada, e aprender com os erros. Empreender sempre envolve riscos, especialmente ao explorar novos neg&oacute;cios e formas de trabalho. Embora a cultura seja um tema vasto, &eacute; imposs&iacute;vel discutir </span><span class="NormalTextRun SpellingErrorV2Themed SCXW150149248 BCX8">intraempreendedorismo</span><span class="NormalTextRun SCXW150149248 BCX8"> sem mencion&aacute;-la.</span></span><span class="EOP SCXW150149248 BCX8" data-ccp-props="{">&nbsp;</span></p>
</div>
<div class="OutlineElement Ltr SCXW150149248 BCX8" style="text-align: justify;">
<p class="Paragraph SCXW150149248 BCX8"><span class="TextRun SCXW150149248 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW150149248 BCX8"><span class="TextRun SCXW105312945 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW105312945 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>A cultura de uma empresa &eacute; moldada pelas pessoas que nela trabalham, refletindo seus pensamentos e valores. Portanto, promover o </span><span class="NormalTextRun SpellingErrorV2Themed SCXW150149248 BCX8">intraempreendedorismo</span><span class="NormalTextRun SCXW150149248 BCX8"> nos leva ao terceiro ponto crucial: os l&iacute;deres. Se os l&iacute;deres n&atilde;o apoiarem e promoverem o </span><span class="NormalTextRun SpellingErrorV2Themed SCXW150149248 BCX8">intraempreendedorismo</span><span class="NormalTextRun SCXW150149248 BCX8">, as barreiras do status quo n&atilde;o ser&atilde;o quebradas. Os l&iacute;deres devem incentivar a comunica&ccedil;&atilde;o entre &aacute;reas, criar ferramentas que estimulem a tomada de riscos e a mentalidade de testes.</span></span><span class="EOP SCXW150149248 BCX8" data-ccp-props="{">&nbsp;</span></p>
