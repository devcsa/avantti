<h3>Diversas Defini&ccedil;&otilde;es de Inova&ccedil;&atilde;o&nbsp;</h3>
<p><span class="TextRun SCXW236060174 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW236060174 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>H&aacute; muitas defini&ccedil;&otilde;es de inova&ccedil;&atilde;o. Em geral, a maioria envolve um elemento de cria&ccedil;&atilde;o e novidade, mas tamb&eacute;m de concretiza&ccedil;&atilde;o e resultados. Um exemplo: "inovar &eacute; explorar novas ideias com resultados". Por isso, o termo "inova&ccedil;&atilde;o orientada a resultados" &eacute; quase redundante, pois uma inova&ccedil;&atilde;o s&oacute; &eacute; verdadeiramente inova&ccedil;&atilde;o se j&aacute; tiver entregado o resultado pretendido. Caso contr&aacute;rio, &eacute; apenas uma inven&ccedil;&atilde;o. No entanto, no atual contexto, o uso do pleonasmo se torna conveniente, pois estamos entrando em um novo ciclo para a inova&ccedil;&atilde;o corporativa, que exigir&aacute; muitos resultados e efic&aacute;cia dos esfor&ccedil;os inovativos. A&ccedil;&otilde;es sem resultados claros estar&atilde;o cada vez mais fora das organiza&ccedil;&otilde;es.&nbsp;</p>
<h3>Revis&atilde;o Hist&oacute;rica da Gest&atilde;o da Inova&ccedil;&atilde;o&nbsp;</h3>
<p><span class="TextRun SCXW236060174 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW236060174 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>Vale a pena fazer uma r&aacute;pida retrospectiva para entender os movimentos da gest&atilde;o da inova&ccedil;&atilde;o nos &uacute;ltimos anos. Inicialmente, a estrutura&ccedil;&atilde;o da inova&ccedil;&atilde;o corporativa focava na gest&atilde;o da criatividade. Essa era a fase das in&uacute;meras sess&otilde;es de idea&ccedil;&atilde;o, quando a import&acirc;ncia da ideia em si era ainda muito alta. Com a percep&ccedil;&atilde;o de que muitas ideias n&atilde;o respondiam &agrave;s necessidades reais dos clientes ou do neg&oacute;cio, as organiza&ccedil;&otilde;es come&ccedil;aram a exigir um claro prop&oacute;sito e direcionamento nos esfor&ccedil;os de inova&ccedil;&atilde;o, dando in&iacute;cio &agrave; fase das estrat&eacute;gias de inova&ccedil;&atilde;o.&nbsp;</p>
<p><span class="TextRun SCXW236060174 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW236060174 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>Por fim, com a crescente percep&ccedil;&atilde;o de que mesmo boas ideias bem direcionadas, se n&atilde;o executadas, permanecem apenas como ideias sem gerar valor, h&aacute; uma press&atilde;o cada vez maior para que esses esfor&ccedil;os se transformem em inova&ccedil;&otilde;es concretas. Em inova&ccedil;&otilde;es de resultado, justificando o pleonasmo do in&iacute;cio do artigo.&nbsp;</p>
<h3>Cadeia de Valor do Esfor&ccedil;o Inovativo&nbsp;</h3>
<p><span class="TextRun SCXW236060174 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW236060174 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>Atualmente, pode-se dizer que existe uma "cadeia de valor" do esfor&ccedil;o inovativo corporativo. O equil&iacute;brio entre as atividades de vis&atilde;o, cria&ccedil;&atilde;o e execu&ccedil;&atilde;o pode ser a chave para um processo de inova&ccedil;&atilde;o mais assertivo e com melhores resultados. O que se observa nos &uacute;ltimos anos &eacute; o aumento da import&acirc;ncia da execu&ccedil;&atilde;o nesse equil&iacute;brio, destacando sua relev&acirc;ncia, especialmente em tempos de grandes mudan&ccedil;as.&nbsp;</p>
<h3>Result-Driven Innovation: O pleonasmo da Inova&ccedil;&atilde;o&nbsp;</h3>
<p><span class="TextRun SCXW236060174 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW236060174 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>O per&iacute;odo atual &eacute; caracterizado por uma das maiores densidades de transforma&ccedil;&atilde;o que j&aacute; vivemos como sociedade organizada. Os impactos e desafios para as organiza&ccedil;&otilde;es s&atilde;o significativos, com forte press&atilde;o sobre o caixa das empresas, que, salvo algumas exce&ccedil;&otilde;es, est&atilde;o reduzindo investimentos. Mesmo assim, os l&iacute;deres empresariais est&atilde;o contando com seus esfor&ccedil;os inovativos para superar a crise.&nbsp;</p>
<h3>Necessidade de Resultados em Inova&ccedil;&atilde;o Corporativa&nbsp;</h3>
<p><span class="TextRun SCXW236060174 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW236060174 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>Nesse contexto, n&atilde;o h&aacute; espa&ccedil;o para processos inovativos que consomem muito tempo e recursos. A inova&ccedil;&atilde;o corporativa precisa cada vez mais ser orientada por resultados, incorporando discuss&otilde;es sobre emiss&atilde;o de notas fiscais, fideliza&ccedil;&atilde;o de clientes, redu&ccedil;&atilde;o de custos, gera&ccedil;&atilde;o de novas fontes de receita em novos mercados, redu&ccedil;&atilde;o de riscos cr&iacute;ticos, entre outros elementos de valor para o neg&oacute;cio.&nbsp;</p>
<p><span class="TextRun SCXW236060174 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW236060174 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>Um dos maiores aprendizados das organiza&ccedil;&otilde;es tem sido a import&acirc;ncia da adaptabilidade para a sustentabilidade do neg&oacute;cio. Isso refor&ccedil;a a necessidade de esfor&ccedil;os inovativos baseados em processos que colham iterativamente pequenos resultados concretos, pois na pr&aacute;tica, a adaptabilidade requer resultados antecipados para o redirecionamento &aacute;gil dos pr&oacute;ximos esfor&ccedil;os.&nbsp;</p>
<h3>Ess&ecirc;ncia da Agilidade&nbsp;</h3>
<p><span class="TextRun SCXW236060174 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW236060174 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>Essa &eacute; a ess&ecirc;ncia da agilidade: n&atilde;o simplesmente avan&ccedil;ar linearmente de forma mais r&aacute;pida, mas progredir iterativamente na "cadeia de valor" da inova&ccedil;&atilde;o, alcan&ccedil;ando pequenos resultados mensur&aacute;veis e, a partir do aprendizado, reiniciando a cadeia para ampliar o valor entregue.&nbsp;</p>
<h3>Cultura de Inova&ccedil;&atilde;o&nbsp;</h3>
<p><span class="TextRun SCXW236060174 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW236060174 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>Esses conceitos j&aacute; existiam antes da pandemia, mas agora ganham mais for&ccedil;a, transformando a inova&ccedil;&atilde;o corporativa. Para um processo inovativo realmente orientado a resultados, al&eacute;m da conex&atilde;o direta com a estrat&eacute;gia e da agilidade/flexibilidade mencionadas, a dissemina&ccedil;&atilde;o do esfor&ccedil;o de inova&ccedil;&atilde;o por toda a organiza&ccedil;&atilde;o se torna cada vez mais imperativa.&nbsp;</p>
<p>Manter a inova&ccedil;&atilde;o isolada do resto da empresa dificulta a prepara&ccedil;&atilde;o da organiza&ccedil;&atilde;o para enfrentar momentos semelhantes aos que vivemos hoje. Essa mudan&ccedil;a, por&eacute;m, n&atilde;o &eacute; simples e exige esfor&ccedil;os para que a inova&ccedil;&atilde;o surja em toda a organiza&ccedil;&atilde;o, al&eacute;m de uma cultura apropriada e a dissemina&ccedil;&atilde;o das compet&ecirc;ncias necess&aacute;rias.&nbsp;</p>
<h3>Import&acirc;ncia da Coopera&ccedil;&atilde;o&nbsp;</h3>
<p><span class="TextRun SCXW236060174 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW236060174 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>Por fim, um ingrediente-chave que tem demonstrado efic&aacute;cia para aumentar a assertividade dos esfor&ccedil;os de inova&ccedil;&atilde;o &eacute; a coopera&ccedil;&atilde;o, seja entre pessoas dentro da organiza&ccedil;&atilde;o ou entre diferentes atores do ecossistema. S&atilde;o raras as discuss&otilde;es sobre inova&ccedil;&atilde;o que n&atilde;o mencionam a relev&acirc;ncia das pessoas no centro do processo inovativo.&nbsp;</p>
<p>Nesse contexto, duas formas de inova&ccedil;&atilde;o corporativa se destacam pelo car&aacute;ter colaborativo: o intraempreendedorismo, como atividade de coopera&ccedil;&atilde;o interna, e a inova&ccedil;&atilde;o aberta, como coopera&ccedil;&atilde;o externa. Esfor&ccedil;os desse tipo, desde que "sem espuma", consolidam-se como instrumentos para aumentar a efici&ecirc;ncia e assertividade das atividades de inova&ccedil;&atilde;o.&nbsp;</p>
<h3>Conclus&atilde;o&nbsp;</h3>
<p><span class="TextRun SCXW236060174 BCX8" lang="PT-BR" xml:lang="PT-BR" data-contrast="auto"><span class="NormalTextRun SCXW236060174 BCX8">&nbsp; &nbsp; &nbsp; &nbsp;</span></span>Esperamos que em breve a inova&ccedil;&atilde;o corporativa incorpore totalmente o mindset de resultado, de modo que o pleonasmo do t&iacute;tulo do artigo perca completamente seu sentido.&nbsp;</p>
<p>&nbsp;</p>
